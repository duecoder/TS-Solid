/*
Vantagens e desvantagens do S.O.L.I.D.

Vantagens:
- Código modular
- Código reutilizável (D.R.Y. - Don't repeat yourself)
- Código testável
- Baixo acoplamento e alta coesão/coerência
- Código expansível
- Separation of concerns
- Fácil manunteção

Desvantagens:
- Complexidade
- Código/projeto muito mais extenso
- Tempo de desenvolvimento aumenta muito

Cuidados:
- Criar o código funcional em primeiro momento
- Após isso, observar onde os principios se encaixam e aplicá-los
- Cuidados com YAGNI e KISS:
--- (You aren't gonna need it)
--- (Keep it simple, Stupid)

*/
